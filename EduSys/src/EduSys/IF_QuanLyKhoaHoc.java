    void fillTable() {
        mapHoTenNV = new NguoiHocDAO().selectHoTenNV();
        try {
            listNH = khDao.selectByPageIndex(pageIndex, listCD.get(cboChuyenDe.getSelectedIndex()).getMaCD());
            if (listNH.isEmpty()) {
                MsgBox.alert(this, "Không còn dữ liệu!!");
                if (statusPage) {
                    pageIndex--;
                    statusPage = false;
                }
                listNH = khDao.selectByPageIndex(pageIndex, listCD.get(cboChuyenDe.getSelectedIndex()).getMaCD());
            }
            model.setRowCount(0);
            for (Object[] kh : listNH) {
                model.addRow(new Object[]{
                    kh[0],
                    kh[3],
                    kh[2],
                    Xdate.toString(Xdate.toDate(kh[4].toString(), "yyyy-MM-dd"), "dd/MM/yyyy"),
                    mapHoTenNV.get(kh[6]),
                    Xdate.toString(Xdate.toDate(kh[7].toString(), "yyyy-MM-dd"), "dd/MM/yyyy")
                });
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi đọc dữ liệu");
        }
    }

