    void setForm(KhoaHoc kh) {
        defaultForm();
        txtKhaiGiang.setText(Xdate.toString(Xdate.toDate(kh.getNgayKG(), "yyyy-MM-dd"), "dd/MM/yyyy"));
        txtNgayTao.setText(Xdate.toString(Xdate.toDate(kh.getNgayTao(), "yyyy-MM-dd"), "dd/MM/yyyy"));
        txtNguoiTao.setText(listNH.get(tblKhoaHoc.getSelectedRow())[6].toString());
        txtGhiChu.setText(listNH.get(tblKhoaHoc.getSelectedRow())[5].toString());
    }

    KhoaHoc getForm() {
        ChuyenDe cd = listCD.get(cboChuyenDe.getSelectedIndex());
        if (row != -1) {
            return new KhoaHoc(tblKhoaHoc.getValueAt(row, 0).toString(), cd.getMaCD(), cd.getHocPhi(), cd.getThoiLuong(), Xdate.toString(Xdate.toDate(txtKhaiGiang.getText(), "dd/MM/yyyy"), "yyyy-MM-dd"), txtGhiChu.getText(), Xdate.toString(new Date(), "yyyy-MM-dd"));
        } else {
            return new KhoaHoc(cd.getMaCD(), cd.getHocPhi(), cd.getThoiLuong(), Xdate.toString(Xdate.toDate(txtKhaiGiang.getText(), "dd/MM/yyyy"), "yyyy-MM-dd"), txtGhiChu.getText(), Auth.user.getMaNv(), Xdate.toString(new Date(), "yyyy-MM-dd"));
        }
    }

    void fillCboKhoaHoc() {
        cboChuyenDe.removeAllItems();
        try {
            for (ChuyenDe cd : listCD) {
                cboChuyenDe.addItem(cd.getTenCD());
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi đọc dữ liệu");
        }
    }

    void clearForm() {
        defaultForm();
        this.row = -1;
        updateStatus();
    }

