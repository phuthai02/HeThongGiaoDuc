    void insert() {
        if (validateForm()) {
            try {
                khDao.insert(getForm());
                clearForm();
                fillTable();
                MsgBox.alert(this, "Thêm khóa học mới thành công");
            } catch (Exception e) {
                if (MsgBox.confirm(this, "Thêm khóa học thất bại!! Bạn có muốn báo lỗi tới nhà phát triển?")) {
                    Xreport.writeException(e, getForm());
                    Xreport.sendMail();
                }
            }
        }
    }

