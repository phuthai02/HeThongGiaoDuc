    void delete() {
        int maKH = Integer.parseInt(tblKhoaHoc.getValueAt(row, 0) + "");
        try {
            if (MsgBox.confirm(this, "Bạn thực sự muốn xóa khóa học này?")) {
                khDao.delete(maKH);
                clearForm();
                fillTable();
                MsgBox.alert(this, "Xóa khóa học thành công");
            }
        } catch (Exception e) {
            if (MsgBox.confirm(this, "Xóa khóa học thất bại!! Bạn có muốn báo lỗi tới nhà phát triển?")) {
                Xreport.writeException(e, maKH);
                Xreport.sendMail();
            }
        }
    }

    void edit() {
        int maKH = Integer.parseInt(tblKhoaHoc.getValueAt(row, 0) + "");
        KhoaHoc kh = khDao.selectById(maKH);
        this.setForm(kh);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void next() {
        if (this.row < tblKhoaHoc.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void last() {
        this.row = tblKhoaHoc.getRowCount() - 1;
        this.edit();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblKhoaHoc.getRowCount() - 1);

        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    boolean validateForm() {
        if (txtKhaiGiang.getText().equals("")) {
            MsgBox.alert(this, "Không để trống ngày khai giảng");
            txtKhaiGiang.requestFocus();
            return false;
        } else {
            try {
                Date date = Xdate.toDate(txtKhaiGiang.getText(), "dd/MM/yyyy");
                if (date.before(new Date())) {
                    MsgBox.alert(this, "Ngày khai giảng phải là ngày sau ngày tạo");
                    txtKhaiGiang.requestFocus();
                    return false;
                } else {
                    return true;
                }
            } catch (Exception e) {
                MsgBox.alert(this, "Ngày khai giảng có dạng dd/MM/yyyy");
                txtKhaiGiang.requestFocus();
                return false;
            }
        }
    }
